    private void jButtonAgregarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarEmpleadoActionPerformed
        // TODO add your handling code here:
        
        String nombreSede= this.jTextNombreSede.getText();
        boolean[] verificar = new boolean[6];
        String Nit = this.jTextNit.getText();
        verificar[0]= this.jTextNit.getText().isEmpty();
        String sede = this.jTextSede.getText();
        verificar[1] = this.jTextSede.getText().isEmpty();
        String cedula = this.jTextCedula.getText();
        verificar[2] =  this.jTextCedula.getText().isEmpty();
        String nombre = this.jTextNombre.getText();
        verificar[3] = this.jTextNombre.getText().isEmpty();
        String contrasena = this.jTextContrasena.getText();
        verificar[4]=this.jTextContrasena.getText().isEmpty();
        String confirmar = this.jTextConfirmada.getText();
        verificar[5]= this.jTextConfirmada.getText().isEmpty();
        
        
        if(!verificar[0] && !verificar[1] && !verificar[2] && !verificar[3] && !verificar[4] && !verificar[5]  ){
            
            if(contrasena.equals(confirmar)){
                
                if(agregarEmpleado(cedula,nombre,contrasena,sede)){
                
                    JOptionPane.showMessageDialog(null ,"El empleado a sido agregado a la sede"+ nombreSede );
                
                 int respuesta = JOptionPane.showConfirmDialog(null ,"Desea Agregar otro empleado a la sede "+ nombreSede,"Agregar Empleado", JOptionPane.YES_NO_OPTION );
                 if(respuesta==JOptionPane.YES_OPTION){
                       
                     AgregarEmpleado nuevo = new AgregarEmpleado();

                     nuevo.setVisible(true);
                     nuevo.jTextNit.setText(Nit);
                     nuevo.jTextNombreSede.setText(nombreSede);
                     nuevo.jTextSede.setText(String.valueOf(sede));

                        }else{
                     
                     int respuesta1 = JOptionPane.showConfirmDialog(null ,"Desea Agregar un receptor a la sede: "+ nombreSede,"Agregar Receptor", JOptionPane.YES_NO_OPTION );
                     
                     if(respuesta==JOptionPane.YES_OPTION){
             
                            AgregarReceptor nuevo = new AgregarReceptor();
                            nuevo.setVisible(true);
                            nuevo.jTextNombreSede.setText(nombreSede);
                            nuevo.jTextSede.setText(sede);

                            this.setVisible(false);
                            
                     }else{
                     MenuAdmin menu = new MenuAdmin();
                     menu.setVisible(true);
                     this.setVisible(false);
                     
                     }

                        }
                
                
                }else{
                
                    JOptionPane.showMessageDialog(null ,"Error. El empleado con la cedula ingresada ya existe" ,"Problema proceso",JOptionPane.ERROR_MESSAGE);
                }
                
            }else{
            JOptionPane.showMessageDialog(null ,"Por favor verifique que la contraseña y la confirmacion sean iguales" );
            }
        
        }else{
        JOptionPane.showMessageDialog(null ,"Por favor verifique que todos los campos estan llenos" );
        }
      
    }//GEN-LAST:event_jButtonAgregarEmpleadoActionPerformed

